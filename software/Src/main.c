/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_I2C3_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
  /* Initialize drivers */
  MX_BOARD_Init();
  MX_MOTOR_Init();
  MX_NUNCHUCK_Init();

  /* Play startup sound */
  HAL_BOARD_BuzzerBeep(&buzzer,440,200);
  HAL_BOARD_BuzzerBeep(&buzzer,440*2,200);
  HAL_BOARD_BuzzerBeep(&buzzer,440*3,200);
  HAL_BOARD_BuzzerBeep(&buzzer,440*4,200);
  HAL_BOARD_BuzzerBeep(&buzzer,440,300);

  /* Start ADC DMA */
  HAL_ADC_Start_DMA(&hadc1, (uint32_t *) adcBuffer, 9);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

	  HAL_NUNCHUCK_Read(&nun1);

	  if((nun1.zButton != 1) || (nun1.cButton == 1))
	  {
		  motorMode = HAL_MOTOR_MODE_BREAK;
	  }
	  else if(nun1.yJoy == 0)
	  {
		  motorMode = HAL_MOTOR_MODE_DISABLE;
	  }
	  else if(nun1.yJoy > 0)
	  {
		  motorMode = HAL_MOTOR_MODE_FORWARD;
		  speed = nun1.yJoy*0xFF;
	  }
	  else if(nun1.yJoy < 0)
	  {
		  motorMode = HAL_MOTOR_MODE_REVERSE;
		  speed = -nun1.yJoy*0xFF;
	  }


	  if(nun1.xJoy > 0)
	  {
		  leftSpeed = speed;
		  rightSpeed = (1.0f-nun1.xJoy)*speed;
	  }
	  else if(nun1.xJoy < 0)
	  {
		  rightSpeed = speed;
		  leftSpeed = (1.0f+nun1.xJoy)*speed;
	  }
	  else
	  {
		  rightSpeed = speed;
		  leftSpeed = speed;
	  }

	  motor1.TargetDuty = rightSpeed;
	  motor3.TargetDuty = rightSpeed;

	  motor2.TargetDuty = leftSpeed;
	  motor4.TargetDuty = leftSpeed;

	  motor1.TargetMotorMode = motorMode;
	  motor2.TargetMotorMode = motorMode;
	  motor3.TargetMotorMode = motorMode;
	  motor4.TargetMotorMode = motorMode;

	  HAL_MOTOR_Update(&motor1);
	  HAL_MOTOR_Update(&motor2);
	  HAL_MOTOR_Update(&motor3);
	  HAL_MOTOR_Update(&motor4);

	  HAL_Delay(20);
  }
  /* USER CODE END 3 */
}

